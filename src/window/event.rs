pub enum Event {
    Resized { width: u32, height: u32 },
    CloseRequested,
    Focused(bool),
    KeyDown(Key),
    KeyUp(Key),
    MouseMoved { x: f32, y: f32 },
    MouseDelta { dx: f32, dy: f32 },
    MouseButtonDown(MouseButton),
    MouseButtonUp(MouseButton),
    Scroll { dx: f32, dy: f32 },
}

